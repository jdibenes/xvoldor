	if (es.info() != Eigen::Success) { return Eigen::MatrixXcd(0, 0); }
	Eigen::MatrixXcd V = es.eigenvectors();
	for (int i = 0; i < V.cols(); ++i) { V.col(i) /= V(0, i); }
