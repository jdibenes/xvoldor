bool solver_gpm_hpc3(float const* p3d_1, float const* p3d_2, float* r_12, float* t_12)
{
	Eigen::Matrix<double, 3, 3> P1 = matrix_from_buffer<double, 3, 3>(p3d_1);
	Eigen::Matrix<double, 3, 3> P2 = matrix_from_buffer<double, 3, 3>(p3d_2);

	Eigen::Matrix<double, Eigen::Dynamic, Eigen::Dynamic> p1 = P1;
	Eigen::Matrix<double, Eigen::Dynamic, Eigen::Dynamic> p2 = P2;

	p1.col(2) /= p1(2, 2);
	p2.col(0) /= p2(2, 0);
	p2.col(1) /= p2(2, 1);
	p2.col(2) /= p2(2, 2);

	Eigen::MatrixXcd q_f = solver_solver_prob_pc_relpose_223122(p1, p2);

	Eigen::Matrix<double, 3, 1> p20 = p2.col(0);
	Eigen::Matrix<double, 3, 1> p21 = p2.col(1);
	Eigen::Matrix<double, 3, 1> p22 = p2.col(2);

	Eigen::Matrix<double, 3, 1> v = p20.cross(p21);

	Eigen::Matrix<double, 3, 3> R;
	Eigen::Matrix<double, 3, 1> t;

	double rerror = std::numeric_limits<double>::infinity();
	bool   set    = false;

	for (int i = 0; i < q_f.cols(); ++i)
	{
	Eigen::Matrix<double, 3, 1> qk = q_f.col(i).real();
	Eigen::Matrix<double, 3, 3> Rv = matrix_R_cayley(qk);
	Eigen::Matrix<double, 3, 1> Rp12 = Rv * p1.col(2);
	Eigen::Matrix<double, 3, 1> Rp11 = Rv * p1.col(1);
	Eigen::Matrix<double, 3, 3> At;
	Eigen::Matrix<double, 3, 1> bt;

	At.row(0) = Rp12.cross(p22);
	At.row(1) = Rp11.cross(p21);
	At.row(2) = v.transpose();

	bt(0) = 0;
	bt(1) = 0;
	bt(2) = -v.transpose() * Rv * P1.col(0);

	Eigen::Matrix<double, 3, 1> tv = At.colPivHouseholderQr().solve(bt);

	double re = (P2.col(2).colwise().hnormalized() - (Rv * P1.col(2) + tv).colwise().hnormalized()).colwise().norm().sum();
		
	if (!std::isfinite(re) || (re >= rerror)) { continue; }

	R = Rv;
	t = tv;
	
	rerror = re;
	set    = true;
	}

	if (!set) { return false; }

	Eigen::Matrix<double, 3, 1> r = vector_r_rodrigues(R);

	matrix_to_buffer(r, r_12);
	matrix_to_buffer(t, t_12);

	return is_valid_pose(r, t);
}
